# Dictionaries (Aka Js Objects)

# Single Dictionary
friends_dict = {
    "name": "Bob",
    "last_name": "Jar",
    "age": 12,
}

# Array of Dictionaries
cars = [
    {'car': 'Ford', 'year': 2005},
    {'car': 'Mitsubishi', 'year': 2000},
    {'car': 'BMW', 'year': 2019},
    {'car': 'VW', 'year': 2011}
]

print(friends_dict)
print(list(friends_dict.keys()))
print(list(friends_dict.values()))
print(type(cars[0]))
